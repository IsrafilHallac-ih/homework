package homework;

public class Course {
	int id;
	String courseName;
	String teacherName;
	String  unitPrice;
	
	
	public Course() {
		
	}


	public Course(int id, String courseName, String teacherName, String unitPrice) {
		super();
		this.id = id;
		this.courseName = courseName;
		this.teacherName = teacherName;
		this.unitPrice = unitPrice;
	}




}
